# математических класса и оборудовать кабинеты для них 
# новыми партами. За каждой партой может сидеть два учащихся. 
# Известно количество учащихся в каждом из трех классов. Выведите 
# наименьшее число парт, которое нужно приобрести для них.
# Input: 20 21 22(ввод чисел НЕ в одну строку)
# Output: 32

first = int(input("Введите число учеников в 1-м классе: "))
second = int(input("Введите число учеников во 2-м классе: "))
third = int(input("Введите число учеников в 3-м классе: "))

# desks_1 = first // 2 + first % 2
