# 2 
desks_1 = (first + 1) // 2
desks_2 = (second + 1) // 2
desks_3 = (third + 1) // 2

print(desks_1 + desks_2 + desks_3)

# 3
class1 = int(input("Введите количество учащихся в первом классе: "))
class2 = int(input("Введите количество учащихся во втором классе: "))
class3 = int(input("Введите количество учащихся в третьем классе: "))

total_students = class1 + class2 + class3
total_desks = (total_students // 2) + (total_students % 2)

print("Необходимо приобрести", total_desks, "парт")

